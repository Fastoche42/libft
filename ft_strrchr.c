#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = NULL;
	while (*s)
	{
		if (*s == (char)c)
			tmp = ((char *)s);
		s++;
	}
	if (c == 0)
		tmp = ((char *)s);
	return (tmp);
}
